from bot.utils.openrouter import call_agent

MODEL = "anthropic/claude-sonnet-4-6"
TEMPERATURE = 0.4

SYSTEM_PROMPT = """Ты — редактор сайта об астрономии и космосе.

Получаешь черновик статьи и улучшаешь его:
- Убираешь воду и повторы
- Режешь длинные предложения
- Проверяешь: цепляет ли первый абзац
- Проверяешь: понятны ли объяснения для неспециалиста
- Проверяешь SEO: есть ли H1, есть ли H2-подзаголовки
- Не меняешь факты и структуру — только улучшаешь форму

Верни только отредактированный текст, без комментариев."""

SEO_REVISION_PROMPT = """Ты — редактор-SEO-специалист сайта об астрономии и космосе.

Получаешь готовую статью и SEO-анализ с конкретными замечаниями.
Твоя задача — применить все SEO-замечания к тексту:
- Добавь ключевые слова туда, где их не хватает — естественно, без спама
- Убедись, что главное ключевое слово стоит в H1 и в первом абзаце
- Проверь, что H2-заголовки содержат ключевые слова или их вариации
- Не меняй факты, смысл и стиль — только SEO-улучшения

Верни только финальный текст статьи, без комментариев."""


def run(draft: str) -> str:
    context = f"""
ЧЕРНОВИК СТАТЬИ:
{draft}

Отредактируй. Верни только готовый текст.
"""
    return call_agent(SYSTEM_PROMPT, context, MODEL, TEMPERATURE)


def run_seo_revision(article: str, seo_feedback: str) -> str:
    context = f"""
СТАТЬЯ:
{article}

SEO-АНАЛИЗ И ЗАМЕЧАНИЯ:
{seo_feedback}

Примени замечания. Верни только финальный текст статьи.
"""
    return call_agent(SEO_REVISION_PROMPT, context, MODEL, TEMPERATURE)
