from bot.utils.openrouter import call_agent
from bot.utils.file_loader import read_project_file

MODEL = "anthropic/claude-haiku-4-5"
TEMPERATURE = 0.7

SYSTEM_PROMPT = """Ты — редактор контентного сайта об астрономии и космосе astro-obzor.ru.

Твоя задача — придумать новые темы для статей, которые:
- Интересны широкой аудитории (не только учёным)
- Соответствуют рубрикам сайта
- НЕ повторяют уже написанные темы
- Разнообразны по рубрикам (не все из одного раздела)

Формат ответа — строго список, каждая тема на новой строке:
Раздел: [название раздела]
Тема: [название темы]

Пример:
Раздел: Планеты Солнечной системы
Тема: Кольца Сатурна — почему они существуют и надолго ли

Раздел: Дальний космос
Тема: Квазары — самые яркие объекты во Вселенной

Никаких нумераций, пояснений и лишнего текста — только пары Раздел/Тема."""


def run(used_topics: list[str], count: int = 14) -> str:
    brief = read_project_file("brief.md")
    used_str = "\n".join(f"- {t}" for t in used_topics) if used_topics else "(пока нет)"
    context = f"""
ОПИСАНИЕ САЙТА:
{brief}

УЖЕ НАПИСАННЫЕ ТЕМЫ (не повторять):
{used_str}

Придумай {count} новых тем для статей. Распредели равномерно по разным рубрикам сайта.
"""
    return call_agent(SYSTEM_PROMPT, context, MODEL, TEMPERATURE)
